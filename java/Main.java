public class Main {
    public static void main(String[] args) {
        System.out.println("7 & 3 : " + (-7 & -3));
        System.out.println("7 | 3 : " + (7 | 3));
        System.out.println("7 ^ 3 : " + (-7 ^ -3));
        System.out.println(" ~7 : " + (~7) + "\r");

        System.out.printf("7 & 4 : %d\n", (7 & 4));
        System.out.printf("7 | 4 : %d\n", (7 | 4));
        System.out.printf("7 ^ 4 : %d\n", (7 ^ 4));
        System.out.printf(" ~4 : %d\n", (~4));
    }
}